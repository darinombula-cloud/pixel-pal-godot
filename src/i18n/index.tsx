interface Ctx { lang: Lang; setLang: (l: Lang) => void; t: (k: string, vars?: Record<string, string | number>) => string; }
const I18nCtx = createContext<Ctx>({ lang: "en", setLang: () => {}, t: (k) => k });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  useEffect(() => {
    try {
      const stored = localStorage.getItem("forge.lang") as Lang | null;
      if (stored && DICT[stored]) { setLangState(stored); return; }
      const nav = (typeof navigator !== "undefined" ? navigator.language.slice(0, 2) : "en") as Lang;
      if (DICT[nav]) setLangState(nav);
    } catch {}
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem("forge.lang", l); } catch {}
    if (typeof document !== "undefined") document.documentElement.lang = l;
  };
  const t = (k: string, vars?: Record<string, string | number>) => {
    let s = DICT[lang][k] ?? DICT.en[k] ?? k;
    if (vars) for (const [vk, vv] of Object.entries(vars)) s = s.replaceAll(`{${vk}}`, String(vv));
    return s;
  };
  return <I18nCtx.Provider value={{ lang, setLang, t }}>{children}</I18nCtx.Provider>;
}

export function useI18n() { return useContext(I18nCtx); }

